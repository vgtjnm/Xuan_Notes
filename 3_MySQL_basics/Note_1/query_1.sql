#MySQL的代码文件我打算取名叫做query，所以你会看到query1,2等等

#mysql里不区分大小写
#但是关键字一般使用大写，其他需要小写

#USE是选择要使用的数据库
#下面代码意思告诉MySQL后续的操作都在名为sql_store的数据库中进行
USE sql_store;

#SELECT选择要查询的列,*是通配符，表示返回表中的所有字段
#下面代码意思选取所有列（字段）
SELECT *

#FROM指定要查询的表
#下面代码意思从customers（客户表）中获取数据
FROM customers

#'--'是MySQL中的单行注释符号
#代码原意是按客户id为1的条件筛选
-- WHERE customer_id = 1

#ORDER BY作用是排序
#下面代码意思是按照first_name（名字）字段进行升序排序（默认）
ORDER BY first_name;

#还有一点是Mysql结束完一段指令后要分号
#分号加在整段SQL语句的最后，表示一条语句的结束

