#varchar和char是有区别的，核心区别在于存储方式：
#CHAR(n)：固定长度，不管实际存了几个字符，永远占 n 个字节。
#比如 CHAR(10) 存 "hi"，会用空格补齐到 10 位。
#VARCHAR(n)：可变长度，实际存几个字符就占几个字节（+1~2字节记录长度）。
#比如 VARCHAR(10) 存 "hi"，只占 2 个字节。
#一句话总结：
#VARCHAR 的额外开销主要在于解析长度字段和可能的内存碎片，现代数据库对此优化很好，通常不是瓶颈。
#不过现在的数据库优化已经很好了，VARCHAR虽然有判断但是耗时也不多，大多是情况可代替CHAR

#接下来是右边的几个东西
#PK是主键，customer_id被标记为了主键，这也是他有黄色小闪电的原因
#下面说下主键的作用：
#唯一标识每一行：主键列的值在整个表中必须唯一，不允许重复，确保每条记录都能被精确定位。
#非空约束：主键列自动禁止 NULL 值，保证每行都有有效的标识符。
#自动创建索引：数据库会在主键列上自动建立聚簇索引（clustered index，如 MySQL InnoDB），大幅加快按主键查询的速度。
#支持外键关联 — 其他表可以通过外键（FOREIGN KEY）引用该列，建立表与表之间的关联关系，是关系型数据库设计的基础。
#数据完整性保障 — 数据库引擎会在插入/更新时自动检查主键约束，防止脏数据进入。
#简单来说，主键 = 唯一 + 非空 + 索引，是表的"身份证"。

#NN是NOT NULL的缩写，决定了该列是否接受空值
